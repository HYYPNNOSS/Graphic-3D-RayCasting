#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*str;
	size_t	i;

	if (!s)
		return (NULL);
	if ((size_t)start > ft_strlen((char *) s))
		return (ft_strdup(""));
	if (ft_strlen((char *)(s + start)) < len)
		len = ft_strlen((char *)(s + start));
	str = (char *) malloc(sizeof(char) * (len + 1));
	if (!str)
		return (0);
	i = 0;
	while (i < len)
	{
		str[i] = *(s + start + i);
		i++;
	}
	str[i] = '\0';
	return (str);
}
